let modals = document.getElementsByTagName("li")
for (i = 0; i < modals.length; i++) {

       modals[i].addEventListener("click", filters)


}



function filters(e) {
       function show() {
              let mode = document.querySelectorAll("li")
              for (i = 0; i < mode.length; i++) {
                     if (mode[i].innerHTML == questions[currentMenu].answer) {
                            mode[i].style.backgroundColor = "green"
                     }
              }
       }

       if (e.target.innerHTML == questions[currentMenu].answer) {

              green = e.target.style.backgroundColor = "green"

              if (green) {
                     scores += 5
                     score.innerHTML = `${scores}`
                     let sound = document.createElement("audio")
                     sound.autoplay = true
                     let source = document.createElement("source")
                     source.src = "mixkit-classic-click-1117.wav"
                     sound.appendChild(source)
              }
              setTimeout(() => {
                     currentMenu++
                     e.target.style.backgroundColor = "black"

                     if (currentMenu > questions.length - 1) {
                            setTimeout(Win, 1000)
                     }
                     Loaded(currentMenu)
              }, 500)

       }
       else {
              let sound = document.createElement("audio")
              sound.autoplay = true
              let source = document.createElement("source")
              source.src = "mixkit-negative-tone-interface-tap-2569.wav"
              sound.appendChild(source)
              e.target.style.backgroundColor = "red"
              setTimeout(show, 150)
              setTimeout(Lose, 300)
       }
}



